package ca.on.gov.gsc.s2i.e2e.acsc.model;

public class FeeModel {
	private String skip;
	private String primary;
	private String liquid;
	private String networth;
	private String numOfInHouse;
	private String grossRange;
	private String expectedFee;
	
	public String getSkip() {
		return skip;
	}
	public void setSkip(String skip) {
		this.skip = skip;
	}
	public String getPrimary() {
		return primary;
	}
	public void setPrimary(String primary) {
		this.primary = primary;
	}
	public String getLiquid() {
		return liquid;
	}
	public void setLiquid(String liquid) {
		this.liquid = liquid;
	}
	public String getNetworth() {
		return networth;
	}
	public void setNetworth(String networth) {
		this.networth = networth;
	}
	public String getNumOfInHouse() {
		return numOfInHouse;
	}
	public void setNumOfInHouse(String numOfInHouse) {
		this.numOfInHouse = numOfInHouse;
	}
	public String getGrossRange() {
		return grossRange;
	}
	public void setGrossRange(String grossRange) {
		this.grossRange = grossRange;
	}
	public String getExpectedFee() {
		return expectedFee;
	}
	public void setExpectedFee(String expectedFee) {
		this.expectedFee = expectedFee;
	}
	
}
